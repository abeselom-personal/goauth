// Package goauth provides functions to handle authentication with tokens and more.
//
// It is designed to be a simple and easy to use package that provides the
// necessary functions to handle authentication in a Go application.
//
// The package provides functions to:
//   - Generate a token for a user
//   - Verify a token and return the user information
//   - Refresh a token
//   - Revoke a token
//
// The package also provides a simple middleware to protect routes
// with authentication.
package goauth
